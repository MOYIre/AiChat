def get_system_prompt_by_mode(mode: str) -> str:
    """
    根据模式获取对应的系统提示词

    Args:
        mode: 模式名称（'chat', 'admin', 'coc', 'dnd', 'think'）

    Returns:
        对应的系统提示词
    """
    prompts = {
        'chat': SYSTEM_PROMPT_CHAT,
        'admin': SYSTEM_PROMPT_ADMIN,
        'coc': SYSTEM_PROMPT_COC,
        'dnd': SYSTEM_PROMPT_DND,
        'think': SYSTEM_PROMPT_THINK
    }
    return prompts.get(mode, SYSTEM_PROMPT_CHAT)
